/* +++++++++ Single Threaded nature of JS ++++++++++++ 
 Single threaded means JavaScript can do only one task at a time.
That means:
    Only one call stack
    Only one instruction executed at a time      
Example:                                        */

console.log(1);
console.log(2);
console.log(3);
console.log(4);
console.log(5);

/* The execution of codes line by line one by one is called Singlethreaded. 
    JavaScript is Single threaded in nature  */
